class fileUpload {
    openid
    fileid
    uploadTime

    constructor() {}

    get openid() {
        return this.openid
    }

    set openid(value) {
        this.openid = value
    }

    get fileid() {
        return this.fileid
    }

    set fileid(value) {
        this.fileid = value
    }

    get uploadTime() {
        return this.uploadTime
    }

    set uploadTime(value) {
        this.uploadTime = value
    }
}